// API Error Types
export interface APIError {
  message: string;
  code?: string;
  status: number;
  details?: unknown;
}

// Request/Response Types
export interface APIResponse<T> {
  success: boolean;
  data?: T;
  error?: APIError;
  timestamp: string;
  requestId: string;
}

